class PosNegDice
{
    public:
    vector<int> evaluateRoll(int t, vector<int> p, vector<int> n)
    {
        int dice[7] = {0};
        for (int i = 0; i < p.size(); ++i) dice[p[i]]++;
        for (int i = 0; i < n.size(); ++i) dice[n[i]]--;

        vector<int> sol;
        sol.push_back(0);
        sol.push_back(0);
        for (int i = 1; i <= 6; ++i)
        {
            if (i <= t && dice[i] > 0) sol[0] = 1;
            if (dice[i] < 0) sol[1] -= dice[i];
        }
        return sol;
    }
};

